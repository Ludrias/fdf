#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*s2;

	s2 = (char *)malloc((n + 1) * sizeof(char));
	ft_strncpy(s2, s1, n);
	s2[n] = '\0';
	return (s2);
}
