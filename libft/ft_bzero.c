#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*bufblanc;

	i = 0;
	bufblanc = (char *)s;
	while (i < n)
	{
		bufblanc[i] = '\0';
		i++;
	}
}
